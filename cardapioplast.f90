SUBROUTINE CARDAPIOPLAST(DTIME,DGAMMA,IPLAST,VAROLD,VAR,PRP,MANDEL,SYI,PHI,DPHIDGAMMA,DPHIDMANDEL,DNORMDMANDEL)
  USE BASFUN
  IMPLICIT REAL(8)(A-H,O-Z)
  REAL(8),PARAMETER::BN=1.0D99
  REAL(8),PARAMETER::PI=4.0D00*ATAN(1.0D00)
  REAL(8)::ALPHA,BETA,PHI
  REAL(8),DIMENSION(*)::PRP,VAROLD,VAR
  REAL(8),DIMENSION(6)::YIELDRATIOS
  REAL(8),DIMENSION(3,3)::MANDEL,DPHIDMANDEL,DNORMDSY
  REAL(8),DIMENSION(3,3,3,3)::DNORMDMANDEL
!----------------------------
!*** AVOID DIVISIONS BY ZERO
!----------------------------
  SY=SYI
  IF(ABS(SY).LE.1.0D-20)SY=BN
!----------------------------------
!*** CALLS SPECIFIC YIELD FUNCTION
!----------------------------------
  SELECT CASE(IPLAST)
  CASE(1)
     DO I=1,6
        YIELDRATIOS(I)=PRP(13+I)
     END DO
     IF(RNORM2(6,YIELDRATIOS).LE.1.0D-20)THEN
        YIELDRATIOS=1.0D00
     END IF
     ALPHA=30.0D00*PI/180.0D00
     BETA=0.0D00*PI/180.0D00
     CALL HILLCRITERION(SY,ALPHA,BETA,YIELDRATIOS,MANDEL,PHI,DPHIDSY,DPHIDMANDEL,DNORMDSY,DNORMDMANDEL)
  CASE(2)
     CALL BARLAT2004(MANDEL,SY,PHI,DPHIDSY,DPHIDMANDEL,DNORMDSY,DNORMDMANDEL)
  CASE(3)
     RN=8.0D00
     CALL HOSFORDCRITERION(SY,RN,MANDEL,PHI,DPHIDSY,DPHIDMANDEL,DNORMDSY,DNORMDMANDEL)
!!     WRITE(*,*)"HOSFORD"
  CASE DEFAULT
     STOP "ERROR PROBLEMS CARDAPIOPLAST"
  END SELECT
!---------------------------------------
!*** CORRECTS WITH STRAIN RATE
!*** DO NOT CHANGE THE FORMULA, PLEASE
!*** THINK BEFORE TOUCHING
!---------------------------------------
  ETA=PRP(23)
  RP=PRP(24)
  IF(ABS(ETA*RP).GT.1.0D-20)THEN
!---------------------------------------
!*** NOTE: DGAMMA UNITS ARE N.M^-2
!---------------------------------------
!     IF(DGAMMA.GT.1.0D-20)THEN
!        WRITE(*,*)"PHI BEFORE",PHI
!        WRITE(*,*)"DGAMMA BEFORE",PHI
     PHI=PHI-((ETA*DGAMMA)/(DTIME*SY*SY)) !**RP
     DPHIDGAMMA=-ETA/(DTIME*SY*SY)
!     DPHIDGAMMA=-(RP*((DGAMMA*ETA)/(DTIME*SY*SY))**RP)/DGAMMA
!        WRITE(*,*)"PHI,DPHIDGAMMA",PHI,DPHIDGAMMA
!     ELSE
!        DPHIDGAMMA=0.0D00
!     END IF
  ELSE
     DPHIDGAMMA=0.0D00
  END IF
END SUBROUTINE CARDAPIOPLAST

